package com.opple.fa.config.dao;

import java.util.List;

import com.opple.fa.config.entity.Supplier;

public interface SupplierDAO {
	/**
	 * 根据供应商名称或编码查询供应商(模糊)
	 * @param supplier
	 * @return
	 */
	List<Supplier> searchSupplier(Supplier supplier);
	
	/**
	 * 根据供应商名称或编码查询供应商(绝对)
	 * @param supplier
	 * @return
	 */
	List<Supplier> searchSupplierByCodeAndName(Supplier supplier);
}
