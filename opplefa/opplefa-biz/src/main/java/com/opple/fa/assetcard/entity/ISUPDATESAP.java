package com.opple.fa.assetcard.entity;

public enum ISUPDATESAP {
	// 已更新
	HASBEENUPDATED("1"),
	// 未更新
	NOTUPDATED("0"),
	// 返回值
	RETURNEDVALUE("0");
	private String isUpdateSap;

	ISUPDATESAP(String isUpdateSap) {
		this.isUpdateSap = isUpdateSap;
	}

	public String getIsUpdateSap() {
		return isUpdateSap;
	}
}
